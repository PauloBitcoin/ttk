if ('serviceWorker' in navigator) {
    navigator.serviceWorker
        .register('./sw.js')
        .then(() => { console.log('Service Worker Registered'); });
}
let deferredPrompt;
const addBtn = document.querySelector('#ctnClickShort button');
addBtn.closest('#ctnClickShort').style.display = 'none';
window.addEventListener('beforeinstallprompt', (e) => {

    // Prevent Chrome 67 and earlier from automatically showing the prompt
    e.preventDefault();
    // Stash the event so it can be triggered later.
    deferredPrompt = e;
    // Update UI to notify the user they can add to home screen
    if (!localStorage.getItem('naoMostrarCreateShort')) {
        addBtn.closest('#ctnClickShort').style.display = 'block';
    }
    addBtn.addEventListener('click', (e) => {
        // hide our user interface that shows our A2HS button
        addBtn.closest('#ctnClickShort').style.display = 'none';
        // Show the prompt
        deferredPrompt.prompt();
        // Wait for the user to respond to the prompt
        deferredPrompt.userChoice.then((choiceResult) => {
            if (choiceResult.outcome === 'accepted') {
                console.log('User accepted the A2HS prompt');
                $.post('./installShort.json', {}, function () { });

            } else {
                console.log('User dismissed the A2HS prompt');
            }
            deferredPrompt = null;
        });
    });
});


function closeCreateShort(e) {
    localStorage.setItem('naoMostrarCreateShort', 'sim');
    $(e).closest('#ctnClickShort').hide();
}